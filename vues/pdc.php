        <h2>Article 1 : Objectif de la Charte</h2>
        <div class="separateur"></div>
        <p>La présente charte a pour objectif de définir les règles de confidentialité qui s'appliquent à
l'échange de lettres entre les élèves, dans le cadre de l'initiative organisée par le CVL.</p>
        <h2>Article 2 : Respect de la Vie Privée</h2>
        <div class="separateur"></div>
        <p>Les élèves s'engagent à respecter la vie privée et la confidentialité des informations
personnelles échangées dans le cadre de cette activité.
Aucune lettre ne doit contenir des informations sensibles ou personnelles pouvant porter
atteinte à la vie privée d'autrui. </p>
        <h2>Article 3 : Utilisation Responsable des Informations</h2>
        <div class="separateur"></div>
        <p>Les élèves s'engagent à utiliser les informations obtenues lors de l'échange de lettres de
manière responsable. Elles ne doivent en aucun cas être divulguées à des tiers, que ce soit
au sein ou en dehors de l'établissement scolaire.</p>
        <h2>Article 4 : Rôle du CVL</h2>
        <div class="separateur"></div>
        <p>Le Conseil de la Vie Lycéenne agit en tant qu'intermédiaire pour la distribution des lettres et
veille au respect des règles de confidentialité énoncées dans la présente charte.</p>
<h2>Article 5 : Consentement</h2>
        <div class="separateur"></div>
        <p>Chaque élève participant à l'échange de lettres via le site donne son consentement explicite
à la distribution de sa lettre par le CVL. Aucune lettre ne sera distribuée sans le
consentement préalable de l'expéditeur.</p>
<h2>Article 6 : Durée de Conservation</h2>
        <div class="separateur"></div>
        <p>Une fois l'événement terminé, le CVL veillera à la destruction sécurisée de toutes les
informations personnelles recueillies dans le cadre de cette initiative. Aucune copie des
lettres ne seront pas conservées après la clôture de l'événement.</h2>
<h2>Article 7 : Sanctions</h2>        
<div class="separateur"></div>
        <p>Tout manquement aux règles énoncées dans la présente charte pourra entraîner des
sanctions appropriées, déterminées par le règlement intérieur du lycée.</p>

<h1 style=" margin-top: 5em;">Signalement des Informations Sensibles à la CPE pour Prévenir le
Harcèlement et Autres Situations Préoccupantes</h1>
        <div class="separateur"></div>
        <p>Dans le cadre de l'échange de lettres entre les élèves, il est primordial de veiller à la sécurité
et au bien-être de chacun. Afin de garantir un environnement sain et respectueux, toute
information sensible susceptible de signaler des situations de harcèlement ou autres
préoccupations doivent être portées à la connaissance de la conseillère Principale d'Éducation
(CPE).</p>
<h2>Article 1 : Définition des Informations Sensibles</h2>
        <div class="separateur"></div>
        <p>Les informations sensibles désignent tout élément présent dans les lettres échangées entre
les élèves pouvant indiquer des situations de détresse, de harcèlement, ou toute autre
problématique susceptible de compromettre le bien-être d'un élève ou d'un étudiant.</p>
<h2>Article 2 : Obligation de Signalement</h2>
        <div class="separateur"></div>
        <p>Tout élève qui, au cours de la lecture des lettres, identifie des informations sensibles est
tenu de les signaler immédiatement à la CPE. Cette démarche vise à assurer la sécurité et
le soutien approprié aux élèves concernés.</p>
<h2>Article 3 : Confidentialité du Signalement</h2>
        <div class="separateur"></div>
        <p>Le processus de signalement des informations sensibles est confidentiel. La CPE s'engage
à traiter ces informations avec la plus grande discrétion, ne les divulguant qu'aux personnes
nécessaires à la résolution de la situation, dans le respect des règles de confidentialité.</p>
<h2>Article 4 : Soutien et Accompagnement</h2>
        <div class="separateur"></div>
        <p>Une fois les informations sensibles signalées, la CPE prendra les mesures nécessaires pour
apporter un soutien approprié aux élèves concernés. Cela peut inclure des entretiens
individuels, des conseils, ou l'orientation vers les services compétents.</p>
